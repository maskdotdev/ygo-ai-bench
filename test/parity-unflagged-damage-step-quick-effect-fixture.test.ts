import { describe, expect, it } from "vitest";
import { createCardReader } from "#engine/data-loaders.js";
import { makeResponseSelector, makeScriptedStep, runScriptedDuelFixture } from "#engine/parity.js";
import type { DuelCardData, ScriptedDuelFixture } from "#duel/types.js";
import { absentAttackGroup, absentEffectGroup, passDamageGroup, turnGroup } from "./parity-legal-action-group-helpers.js";

describe("EDOPro parity unflagged damage-step quick-effect fixtures", () => {
  it("does not offer unflagged quick effects during damage-step battle windows", () => {
    const cards: DuelCardData[] = [
      { code: "100", name: "Battle Attacker", kind: "monster", attack: 1800, defense: 1200 },
      { code: "300", name: "Unflagged Quick", kind: "monster", attack: 500, defense: 500 },
    ];
    const fixture: ScriptedDuelFixture = {
      name: "unflagged damage-step quick effect gate fixture",
      options: { seed: 76, startingHandSize: 2 },
      decks: {
        0: { main: ["100", "300"] },
        1: { main: ["300", "300"] },
      },
      setup: {
        moveCards: [{ player: 0, code: "100", from: "hand", to: "monsterZone", position: "faceUpAttack" }],
        effects: [
          {
            id: "fixture-unflagged-quick",
            player: 0,
            code: "300",
            location: "hand",
            event: "quick",
            range: ["hand"],
            oncePerTurn: true,
            logMessage: "Fixture unflagged quick resolved",
          },
        ],
      },
      responses: [
        makeScriptedStep(makeResponseSelector("changePhase", 0, { phase: "battle" })),
        makeScriptedStep(makeResponseSelector("declareAttack", 0, { attackerUid: "p0-deck-100-0" })),
        makeScriptedStep(makeResponseSelector("passAttack", 1)),
        makeScriptedStep(makeResponseSelector("passAttack", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1), {
          snapshotRestore: "after",
          after: {
            source: "edopro",
            note: "EDOPro does not expose ordinary unflagged quick effects once the Damage Step starts",
            waitingFor: 0,
            windowId: 5,
            windowKind: "battle",
            battleWindow: { kind: "startDamageStep", step: "damage", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            absentLegalActions: [{ type: "activateEffect", player: 0, windowId: 5, windowKind: "battle", effectId: "fixture-unflagged-quick" }],
            absentLegalActionGroups: [absentEffectGroup(0, "fixture-unflagged-quick", 5)],
            legalActions: [{ type: "passDamage", player: 0, windowId: 5, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(0, 1, 5)],
          },
        }),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1), {
          snapshotRestore: "after",
          after: {
            source: "edopro",
            note: "EDOPro keeps unflagged quick effects unavailable before damage calculation",
            waitingFor: 0,
            windowId: 7,
            windowKind: "battle",
            battleWindow: { kind: "beforeDamageCalculation", step: "damage", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            absentLegalActions: [{ type: "activateEffect", player: 0, windowId: 7, windowKind: "battle", effectId: "fixture-unflagged-quick" }],
            absentLegalActionGroups: [absentEffectGroup(0, "fixture-unflagged-quick", 7)],
            legalActions: [{ type: "passDamage", player: 0, windowId: 7, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(0, 1, 7)],
          },
        }),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro keeps during-damage-calculation opponent priority restorable without exposing unflagged quick effects",
            phase: "battle",
            waitingFor: 1,
            windowId: 8,
            windowKind: "battle",
            pendingBattle: true,
            battleStep: "damageCalculation",
            battleWindow: { kind: "duringDamageCalculation", step: "damageCalculation", attackerUid: "p0-deck-100-0", responsePlayer: 1 },
            damagePasses: [],
            legalActionCounts: { 0: 0, 1: 1 },
            legalActionGroupCounts: { 0: 0, 1: 1 },
            absentLegalActions: [{ type: "activateEffect", player: 0, windowId: 8, windowKind: "battle", effectId: "fixture-unflagged-quick" }],
            absentLegalActionGroups: [absentEffectGroup(0, "fixture-unflagged-quick", 8)],
            legalActions: [{ type: "passDamage", player: 1, windowId: 8, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(1, 1, 8)],
          },
          after: {
            source: "edopro",
            note: "EDOPro also keeps unflagged quick effects unavailable during damage calculation",
            waitingFor: 0,
            windowId: 9,
            windowKind: "battle",
            pendingBattle: true,
            battleStep: "damageCalculation",
            battleWindow: { kind: "duringDamageCalculation", step: "damageCalculation", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            absentLegalActions: [{ type: "activateEffect", player: 0, windowId: 9, windowKind: "battle", effectId: "fixture-unflagged-quick" }],
            absentLegalActionGroups: [absentEffectGroup(0, "fixture-unflagged-quick", 9)],
            legalActions: [{ type: "passDamage", player: 0, windowId: 9, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(0, 1, 9)],
          },
        }),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1), {
          snapshotRestore: "after",
          after: {
            source: "edopro",
            note: "EDOPro keeps unflagged quick effects unavailable after damage calculation",
            waitingFor: 0,
            windowId: 11,
            windowKind: "battle",
            battleWindow: { kind: "afterDamageCalculation", step: "damage", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            absentLegalActions: [{ type: "activateEffect", player: 0, windowId: 11, windowKind: "battle", effectId: "fixture-unflagged-quick" }],
            absentLegalActionGroups: [absentEffectGroup(0, "fixture-unflagged-quick", 11)],
            legalActions: [{ type: "passDamage", player: 0, windowId: 11, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(0, 1, 11)],
          },
        }),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1), {
          snapshotRestore: "after",
          after: {
            source: "edopro",
            note: "EDOPro keeps unflagged quick effects unavailable at end damage step",
            waitingFor: 0,
            windowId: 13,
            windowKind: "battle",
            battleWindow: { kind: "endDamageStep", step: "damage", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            absentLegalActions: [{ type: "activateEffect", player: 0, windowId: 13, windowKind: "battle", effectId: "fixture-unflagged-quick" }],
            absentLegalActionGroups: [absentEffectGroup(0, "fixture-unflagged-quick", 13)],
            legalActions: [{ type: "passDamage", player: 0, windowId: 13, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(0, 1, 13)],
          },
        }),
        makeScriptedStep(makeResponseSelector("passDamage", 0), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro keeps the final end-damage-step turn-player pass restorable without exposing unflagged quick effects",
            phase: "battle",
            waitingFor: 0,
            pendingBattle: true,
            currentAttack: true,
            windowId: 13,
            windowKind: "battle",
            battleStep: "damage",
            battleWindow: { kind: "endDamageStep", step: "damage", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            damagePasses: [1],
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            absentLegalActions: [{ type: "activateEffect", player: 0, windowId: 13, windowKind: "battle", effectId: "fixture-unflagged-quick" }],
            absentLegalActionGroups: [absentEffectGroup(0, "fixture-unflagged-quick", 13)],
            legalActions: [{ type: "passDamage", player: 0, windowId: 13, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(0, 1, 13)],
          },
          after: {
            source: "edopro",
            note: "EDOPro resolves battle normally after players pass every damage-step window",
            waitingFor: 0,
            pendingBattle: false,
            currentAttack: false,
            windowId: 14,
            windowKind: "open",
            battleWindow: null,
            lifePoints: { 1: 6200 },
            battleDamage: { 1: 1800 },
            attacksDeclared: ["p0-deck-100-0"],
            legalActionCounts: { 0: 2, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [
              { type: "changePhase", player: 0, windowId: 14, windowKind: "open", count: 1 },
              { type: "endTurn", player: 0, windowId: 14, windowKind: "open", count: 1 },
            ],
            legalActionGroups: [turnGroup(14)],
            absentLegalActions: [
              { type: "activateEffect", player: 0, effectId: "fixture-unflagged-quick" },
              { type: "declareAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 14, windowKind: "open" },
            ],
            absentLegalActionGroups: [absentEffectGroup(0, "fixture-unflagged-quick", 14), absentAttackGroup("p0-deck-100-0", undefined, undefined, 14)],
          },
        }),
      ],
      expected: {
        source: "edopro",
        note: "EDOPro final fixture state never exposed the unflagged quick effect during damage-step timing",
        phase: "battle",
        waitingFor: 0,
        pendingBattle: false,
        currentAttack: false,
        windowId: 14,
        battleWindow: null,
        lifePoints: { 1: 6200 },
        battleDamage: { 1: 1800 },
        attacksDeclared: ["p0-deck-100-0"],
        legalActionCounts: { 0: 2, 1: 0 },
        legalActionGroupCounts: { 0: 1, 1: 0 },
        legalActions: [
          { type: "changePhase", player: 0, windowId: 14, windowKind: "open", count: 1 },
          { type: "endTurn", player: 0, windowId: 14, windowKind: "open", count: 1 },
        ],
        legalActionGroups: [turnGroup(14)],
        absentLegalActions: [
          { type: "activateEffect", player: 0, effectId: "fixture-unflagged-quick" },
          { type: "declareAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 14, windowKind: "open" },
        ],
        absentLegalActionGroups: [absentEffectGroup(0, "fixture-unflagged-quick", 14), absentAttackGroup("p0-deck-100-0", undefined, undefined, 14)],
      },
    };

    expect(runScriptedDuelFixture(fixture, { cardReader: createCardReader(cards) })).toEqual({ ok: true, failures: [] });
  });
});
