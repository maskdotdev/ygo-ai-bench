import { describe, expect, it } from "vitest";
import { createCardReader } from "#engine/data-loaders.js";
import { makeResponseSelector, makeScriptedStep, runScriptedDuelFixture } from "#engine/parity.js";
import type { DuelCardData, ScriptedDuelFixture } from "#duel/types.js";
import {
  absentAttackGroup,
  absentPassBattleGroup,
  absentWindowEffectGroup,
  effectGroup,
  passDamageGroup,
  turnGroup,
} from "./parity-legal-action-group-helpers.js";

describe("EDOPro parity opponent battle damage-calculation quick-effect fixtures", () => {
  it("offers opponent damage-calculation quick effects during damage calculation", () => {
    const cards: DuelCardData[] = [
      { code: "100", name: "Battle Attacker", kind: "monster", attack: 1800, defense: 1200 },
      { code: "300", name: "Opponent Damage Calculation Quick", kind: "monster", attack: 500, defense: 500 },
    ];
    const fixture: ScriptedDuelFixture = {
      name: "opponent damage calculation quick effect fixture",
      options: { seed: 78, startingHandSize: 2 },
      decks: {
        0: { main: ["100", "300"] },
        1: { main: ["300", "300"] },
      },
      setup: {
        moveCards: [{ player: 0, code: "100", from: "hand", to: "monsterZone", position: "faceUpAttack" }],
        effects: [
          {
            id: "fixture-opponent-damage-calculation-quick",
            player: 1,
            code: "300",
            location: "hand",
            event: "quick",
            range: ["hand"],
            oncePerTurn: true,
            property: 0x8000,
            logMessage: "Fixture opponent damage-calculation quick resolved",
          },
        ],
      },
      responses: [
        makeScriptedStep(makeResponseSelector("changePhase", 0, { phase: "battle" })),
        makeScriptedStep(makeResponseSelector("declareAttack", 0, { attackerUid: "p0-deck-100-0" })),
        makeScriptedStep(makeResponseSelector("passAttack", 1)),
        makeScriptedStep(makeResponseSelector("passAttack", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro keeps the turn-player before-damage-calculation pass restorable before entering opponent damage-calculation priority",
            waitingFor: 0,
            windowId: 7,
            windowKind: "battle",
            pendingBattle: true,
            battleStep: "damage",
            battleWindow: { kind: "beforeDamageCalculation", step: "damage", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            damagePasses: [1],
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [{ type: "passDamage", player: 0, windowId: 7, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(0, 1, 7)],
            absentLegalActions: [
              { type: "activateEffect", player: 1, windowId: 7, windowKind: "battle", effectId: "fixture-opponent-damage-calculation-quick" },
              { type: "passDamage", player: 1, windowId: 7, windowKind: "battle" },
            ],
            absentLegalActionGroups: [
              absentWindowEffectGroup(1, "fixture-opponent-damage-calculation-quick", 7, "battle"),
              absentPassBattleGroup(1, "passDamage", 7),
            ],
          },
          after: {
            source: "edopro",
            note: "EDOPro gives the non-turn player first response during damage calculation and exposes their damage-calculation fast effects",
            waitingFor: 1,
            windowId: 8,
            windowKind: "battle",
            pendingBattle: true,
            battleStep: "damageCalculation",
            battleWindow: { kind: "duringDamageCalculation", step: "damageCalculation", attackerUid: "p0-deck-100-0", responsePlayer: 1 },
            damagePasses: [],
            legalActionCounts: { 0: 0, 1: 2 },
            legalActionGroupCounts: { 0: 0, 1: 2 },
            legalActions: [
              { type: "activateEffect", player: 1, windowId: 8, windowKind: "battle", effectId: "fixture-opponent-damage-calculation-quick", count: 1 },
              { type: "passDamage", player: 1, windowId: 8, windowKind: "battle", count: 1 },
            ],
            legalActionGroups: [effectGroup(1, "fixture-opponent-damage-calculation-quick", 1, 8), passDamageGroup(1, 1, 8)],
          },
        }),
        makeScriptedStep(makeResponseSelector("activateEffect", 1, { effectId: "fixture-opponent-damage-calculation-quick" }), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro keeps the opponent damage-calculation quick-effect window restorable before activation",
            waitingFor: 1,
            windowId: 8,
            windowKind: "battle",
            pendingBattle: true,
            battleStep: "damageCalculation",
            battleWindow: { kind: "duringDamageCalculation", step: "damageCalculation", attackerUid: "p0-deck-100-0", responsePlayer: 1 },
            chain: [],
            chainPasses: [],
            damagePasses: [],
            legalActionCounts: { 0: 0, 1: 2 },
            legalActionGroupCounts: { 0: 0, 1: 2 },
            legalActions: [
              { type: "activateEffect", player: 1, windowId: 8, windowKind: "battle", effectId: "fixture-opponent-damage-calculation-quick", count: 1 },
              { type: "passDamage", player: 1, windowId: 8, windowKind: "battle", count: 1 },
            ],
            legalActionGroups: [effectGroup(1, "fixture-opponent-damage-calculation-quick", 1, 8), passDamageGroup(1, 1, 8)],
            absentLegalActions: [{ type: "passDamage", player: 0, windowId: 8, windowKind: "battle" }],
            absentLegalActionGroups: [absentPassBattleGroup(0, "passDamage", 8)],
          },
          after: {
            source: "edopro",
            note: "EDOPro resumes damage calculation timing after the opponent's damage-calculation fast effect resolves",
            waitingFor: 1,
            windowId: 9,
            windowKind: "battle",
            pendingBattle: true,
            battleStep: "damageCalculation",
            battleWindow: { kind: "duringDamageCalculation", step: "damageCalculation", attackerUid: "p0-deck-100-0", responsePlayer: 1 },
            damagePasses: [],
            legalActionCounts: { 0: 0, 1: 1 },
            legalActionGroupCounts: { 0: 0, 1: 1 },
            legalActions: [{ type: "passDamage", player: 1, windowId: 9, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(1, 1, 9)],
            absentLegalActions: [{ type: "activateEffect", player: 1, windowId: 9, windowKind: "battle", effectId: "fixture-opponent-damage-calculation-quick" }],
            absentLegalActionGroups: [
              { player: 1, label: "Effects", windowId: 9, windowKind: "battle", actions: [{ type: "activateEffect", player: 1, windowId: 9, windowKind: "battle", effectId: "fixture-opponent-damage-calculation-quick" }] },
            ],
            logIncludes: ["Fixture opponent damage-calculation quick resolved"],
          },
        }),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro keeps end-damage-step turn-player priority restorable before applying battle damage after the opponent's quick effect",
            waitingFor: 0,
            windowId: 14,
            windowKind: "battle",
            pendingBattle: true,
            battleStep: "damage",
            battleWindow: { kind: "endDamageStep", step: "damage", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            damagePasses: [1],
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [{ type: "passDamage", player: 0, windowId: 14, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(0, 1, 14)],
            absentLegalActions: [
              { type: "activateEffect", player: 1, windowId: 14, windowKind: "battle", effectId: "fixture-opponent-damage-calculation-quick" },
              { type: "passDamage", player: 1, windowId: 14, windowKind: "battle" },
            ],
            absentLegalActionGroups: [
              absentWindowEffectGroup(1, "fixture-opponent-damage-calculation-quick", 14, "battle"),
              absentPassBattleGroup(1, "passDamage", 14),
            ],
          },
          after: {
            source: "edopro",
            note: "EDOPro resolves battle normally after the opponent's damage-calculation fast effect resolves",
            waitingFor: 0,
            pendingBattle: false,
            currentAttack: false,
            windowId: 15,
            windowKind: "open",
            battleWindow: null,
            lifePoints: { 1: 6200 },
            battleDamage: { 1: 1800 },
            attacksDeclared: ["p0-deck-100-0"],
            legalActionCounts: { 0: 2, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [
              { type: "changePhase", player: 0, windowId: 15, windowKind: "open", count: 1 },
              { type: "endTurn", player: 0, windowId: 15, windowKind: "open", count: 1 },
            ],
            legalActionGroups: [turnGroup(15)],
            absentLegalActions: [{ type: "declareAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 15, windowKind: "open" }],
            absentLegalActionGroups: [absentAttackGroup("p0-deck-100-0", undefined, undefined, 15)],
            logIncludes: ["Fixture opponent damage-calculation quick resolved", "Direct attack"],
          },
        }),
      ],
      expected: {
        source: "edopro",
        note: "EDOPro final fixture state applies direct damage after the opponent's damage-calculation fast effect resolves",
        phase: "battle",
        waitingFor: 0,
        pendingBattle: false,
        currentAttack: false,
        windowId: 15,
        windowKind: "open",
        battleWindow: null,
        lifePoints: { 1: 6200 },
        battleDamage: { 1: 1800 },
        attacksDeclared: ["p0-deck-100-0"],
        legalActionCounts: { 0: 2, 1: 0 },
        legalActionGroupCounts: { 0: 1, 1: 0 },
        legalActions: [
          { type: "changePhase", player: 0, windowId: 15, windowKind: "open", count: 1 },
          { type: "endTurn", player: 0, windowId: 15, windowKind: "open", count: 1 },
        ],
        legalActionGroups: [turnGroup(15)],
        absentLegalActions: [{ type: "declareAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 15, windowKind: "open" }],
        absentLegalActionGroups: [absentAttackGroup("p0-deck-100-0", undefined, undefined, 15)],
        logIncludes: ["Fixture opponent damage-calculation quick resolved", "Direct attack"],
      },
    };

    expect(runScriptedDuelFixture(fixture, { cardReader: createCardReader(cards) })).toEqual({ ok: true, failures: [] });
  });
});
