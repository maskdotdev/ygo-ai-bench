import { describe, expect, it } from "vitest";
import { createCardReader } from "#engine/data-loaders.js";
import { makeResponseSelector, makeScriptedStep, runScriptedDuelFixture } from "#engine/parity.js";
import type { DuelCardData, ScriptedDuelFixture } from "#duel/types.js";
import {
  absentOpenAttackGroup,
  effectGroup,
  passBattleGroup,
  passDamageGroup,
  turnGroup,
} from "./parity-legal-action-group-helpers.js";

describe("EDOPro parity battle direct replay fixtures", () => {
  it("replays directly when the original target leaves and no targets remain", () => {
    const cards: DuelCardData[] = [
      { code: "100", name: "Replay Attacker", kind: "monster", attack: 1800, defense: 1200 },
      { code: "200", name: "Replay Target", kind: "monster", attack: 1000, defense: 1000 },
      { code: "300", name: "Target Remover", kind: "monster", attack: 500, defense: 500 },
    ];
    const fixture: ScriptedDuelFixture = {
      name: "target leaves replay direct attack fixture",
      options: { seed: 71, startingHandSize: 2 },
      decks: {
        0: { main: ["100", "300"] },
        1: { main: ["200", "200"] },
      },
      setup: {
        moveCards: [
          { player: 0, code: "100", from: "hand", to: "monsterZone", position: "faceUpAttack" },
          { player: 1, code: "200", from: "hand", to: "monsterZone", position: "faceUpAttack" },
        ],
        effects: [
          {
            id: "fixture-remove-target-before-direct-replay",
            player: 0,
            code: "300",
            location: "hand",
            event: "quick",
            range: ["hand"],
            oncePerTurn: true,
            moveCardsOnResolve: [{ player: 1, code: "200", from: "monsterZone", to: "graveyard" }],
            logMessage: "Fixture target left before direct replay",
          },
        ],
      },
      responses: [
        makeScriptedStep(makeResponseSelector("changePhase", 0, { phase: "battle" })),
        makeScriptedStep(makeResponseSelector("declareAttack", 0, { attackerUid: "p0-deck-100-0", targetUid: "p1-deck-200-1" })),
        makeScriptedStep(makeResponseSelector("passAttack", 1)),
        makeScriptedStep(makeResponseSelector("activateEffect", 0, { effectId: "fixture-remove-target-before-direct-replay" }), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro keeps turn-player attack-response priority restorable before the target-removal quick effect",
            waitingFor: 0,
            windowId: 3,
            windowKind: "battle",
            pendingBattle: true,
            currentAttack: true,
            battleWindow: { kind: "attackNegationResponse", step: "attack", attackerUid: "p0-deck-100-0", targetUid: "p1-deck-200-1", responsePlayer: 0 },
            attackPasses: [1],
            legalActionCounts: { 0: 2, 1: 0 },
            legalActionGroupCounts: { 0: 2, 1: 0 },
            legalActions: [
              { type: "activateEffect", player: 0, windowId: 3, windowKind: "battle", effectId: "fixture-remove-target-before-direct-replay", count: 1 },
              { type: "passAttack", player: 0, windowId: 3, windowKind: "battle", count: 1 },
            ],
            legalActionGroups: [
              effectGroup(0, "fixture-remove-target-before-direct-replay", 1, 3),
              passBattleGroup(0, "passAttack", 1, 3),
            ],
          },
        }),
        makeScriptedStep(makeResponseSelector("passAttack", 1)),
        makeScriptedStep(makeResponseSelector("passAttack", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro keeps the final end-damage-step pass restorable before opening direct replay choices",
            waitingFor: 0,
            windowId: 15,
            windowKind: "battle",
            pendingBattle: true,
            currentAttack: true,
            battleWindow: { kind: "endDamageStep", step: "damage", attackerUid: "p0-deck-100-0", targetUid: "p1-deck-200-1", responsePlayer: 0 },
            damagePasses: [1],
            locations: { monsterZone: ["100"], graveyard: ["200"] },
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [{ type: "passDamage", player: 0, windowId: 15, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(0, 1, 15)],
          },
          after: {
            source: "edopro",
            note: "EDOPro offers direct replay when the original attack target leaves and no opponent monsters remain",
            waitingFor: 0,
            windowId: 16,
            windowKind: "battle",
            pendingBattle: true,
            currentAttack: true,
            battleWindow: { kind: "replayDecision", step: "attack", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            locations: { monsterZone: ["100"], graveyard: ["200"] },
            legalActionCounts: { 0: 2, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [
              { type: "cancelAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 16, windowKind: "battle", count: 1 },
              { type: "replayAttack", player: 0, attackerUid: "p0-deck-100-0", directAttack: true, windowId: 16, windowKind: "battle", count: 1 },
            ],
            absentLegalActions: [{ type: "passDamage", player: 0, windowId: 16, windowKind: "battle" }],
            legalActionGroups: [
              {
                player: 0,
                label: "Attacks",
                windowId: 16,
                windowKind: "battle",
                count: 1,
                actions: [
                  { type: "cancelAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 16, windowKind: "battle", count: 1 },
                  { type: "replayAttack", player: 0, attackerUid: "p0-deck-100-0", directAttack: true, windowId: 16, windowKind: "battle", count: 1 },
                ],
              },
            ],
            absentLegalActionGroups: [
              {
                player: 0,
                label: "Pass",
                windowId: 16,
                windowKind: "battle",
                actions: [{ type: "passDamage", player: 0, windowId: 16, windowKind: "battle" }],
              },
            ],
            logIncludes: ["Replay decision pending"],
          },
        }),
        makeScriptedStep(makeResponseSelector("replayAttack", 0, { attackerUid: "p0-deck-100-0" }), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro exposes no-target replayAttack as the direct replay choice",
            waitingFor: 0,
            pendingBattle: true,
            windowId: 16,
            windowKind: "battle",
            battleWindow: { kind: "replayDecision", step: "attack", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            legalActionCounts: { 0: 2, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [
              { type: "cancelAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 16, windowKind: "battle", count: 1 },
              { type: "replayAttack", player: 0, attackerUid: "p0-deck-100-0", directAttack: true, windowId: 16, windowKind: "battle", count: 1 },
            ],
            legalActionGroups: [
              {
                player: 0,
                label: "Attacks",
                windowId: 16,
                windowKind: "battle",
                count: 1,
                actions: [
                  { type: "cancelAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 16, windowKind: "battle", count: 1 },
                  { type: "replayAttack", player: 0, attackerUid: "p0-deck-100-0", directAttack: true, windowId: 16, windowKind: "battle", count: 1 },
                ],
              },
            ],
          },
          after: {
            source: "edopro",
            note: "EDOPro reopens attack responses after choosing direct replay",
            waitingFor: 1,
            windowId: 17,
            windowKind: "battle",
            pendingBattle: true,
            currentAttack: true,
            battleWindow: { kind: "attackNegationResponse", step: "attack", attackerUid: "p0-deck-100-0", responsePlayer: 1 },
            legalActionCounts: { 0: 0, 1: 1 },
            legalActionGroupCounts: { 0: 0, 1: 1 },
            legalActions: [{ type: "passAttack", player: 1, windowId: 17, windowKind: "battle", count: 1 }],
            legalActionGroups: [
              {
                player: 1,
                label: "Pass",
                windowId: 17,
                windowKind: "battle",
                count: 1,
                actions: [{ type: "passAttack", player: 1, windowId: 17, windowKind: "battle", count: 1 }],
              },
            ],
            logIncludes: ["Replayed direct attack"],
          },
        }),
        makeScriptedStep(makeResponseSelector("passAttack", 1)),
        makeScriptedStep(makeResponseSelector("passAttack", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro keeps the replayed direct attack's final end-damage-step pass restorable before applying damage",
            waitingFor: 0,
            windowId: 28,
            windowKind: "battle",
            pendingBattle: true,
            currentAttack: true,
            battleWindow: { kind: "endDamageStep", step: "damage", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            damagePasses: [1],
            locations: { monsterZone: ["100"], graveyard: ["200"] },
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [{ type: "passDamage", player: 0, windowId: 28, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(0, 1, 28)],
          },
          after: {
            source: "edopro",
            note: "EDOPro resolves the direct replay as direct battle damage after both players pass battle windows",
            waitingFor: 0,
            pendingBattle: false,
            currentAttack: false,
            windowId: 29,
            windowKind: "open",
            battleWindow: null,
            lifePoints: { 1: 6200 },
            battleDamage: { 1: 1800 },
            locations: { monsterZone: ["100"], graveyard: ["200"] },
            legalActionCounts: { 0: 2, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [
              { type: "changePhase", player: 0, windowId: 29, windowKind: "open", count: 1 },
              { type: "endTurn", player: 0, windowId: 29, windowKind: "open", count: 1 },
            ],
            legalActionGroups: [turnGroup(29)],
            absentLegalActions: [{ type: "declareAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 29, windowKind: "open" }],
            absentLegalActionGroups: [absentOpenAttackGroup(0, "p0-deck-100-0", 29)],
          },
        }),
      ],
      expected: {
        source: "edopro",
        note: "EDOPro final fixture state resolves direct replay damage while preserving the original target history",
        phase: "battle",
        waitingFor: 0,
        pendingBattle: false,
        currentAttack: false,
        battleWindow: null,
        attacksDeclared: ["p0-deck-100-0"],
        attackedTargetUids: ["p1-deck-200-1"],
        battlePairs: [{ attackerUid: "p0-deck-100-0", targetUid: "p1-deck-200-1" }],
        lifePoints: { 1: 6200 },
        battleDamage: { 1: 1800 },
        locations: { monsterZone: ["100"], graveyard: ["200"] },
        windowId: 29,
        windowKind: "open",
        legalActionCounts: { 0: 2, 1: 0 },
        legalActionGroupCounts: { 0: 1, 1: 0 },
        legalActions: [
          { type: "changePhase", player: 0, windowId: 29, windowKind: "open", count: 1 },
          { type: "endTurn", player: 0, windowId: 29, windowKind: "open", count: 1 },
        ],
        legalActionGroups: [turnGroup(29)],
        absentLegalActions: [{ type: "declareAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 29, windowKind: "open" }],
        absentLegalActionGroups: [absentOpenAttackGroup(0, "p0-deck-100-0", 29)],
        logIncludes: ["Fixture target left before direct replay", "Replay decision pending", "Replayed direct attack"],
      },
    };

    expect(runScriptedDuelFixture(fixture, { cardReader: createCardReader(cards) })).toEqual({ ok: true, failures: [] });
  });
});
