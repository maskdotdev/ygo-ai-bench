import { describe, expect, it } from "vitest";
import { createCardReader } from "#engine/data-loaders.js";
import { makeResponseSelector, makeScriptedStep, runScriptedDuelFixture } from "#engine/parity.js";
import type { DuelCardData, ScriptedDuelFixture } from "#duel/types.js";
import { absentAttackGroup, absentOpenAttackGroup, effectGroup, passBattleGroup, passDamageGroup, replayAttackGroup, turnGroup } from "./parity-legal-action-group-helpers.js";

describe("EDOPro parity battle only-monster replay fixtures", () => {
  it("does not offer direct replay for only-monster attackers", () => {
    const cards: DuelCardData[] = [
      { code: "100", name: "Only Monster Replay Attacker", kind: "monster", attack: 1800, defense: 1200 },
      { code: "200", name: "Removed Monster Replay Target", kind: "monster", attack: 1000, defense: 1000 },
      { code: "300", name: "Only Monster Target Remover", kind: "monster", attack: 500, defense: 500 },
    ];
    const fixture: ScriptedDuelFixture = {
      name: "only monster replay choice fixture",
      options: { seed: 178, startingHandSize: 2 },
      decks: {
        0: { main: ["100", "300"] },
        1: { main: ["200"] },
      },
      setup: {
        moveCards: [
          { player: 0, code: "100", from: "hand", to: "monsterZone", position: "faceUpAttack" },
          { player: 1, code: "200", from: "hand", to: "monsterZone", position: "faceUpAttack" },
        ],
        effects: [
          {
            id: "fixture-only-monster-replay",
            player: 0,
            code: "100",
            location: "monsterZone",
            event: "continuous",
            effectCode: 343,
            range: ["monsterZone"],
          },
          {
            id: "fixture-remove-only-monster-target",
            player: 0,
            code: "300",
            location: "hand",
            event: "quick",
            range: ["hand"],
            oncePerTurn: true,
            moveCardsOnResolve: [{ player: 1, code: "200", from: "monsterZone", to: "graveyard" }],
            logMessage: "Fixture only-monster target left before replay",
          },
        ],
      },
      responses: [
        makeScriptedStep(makeResponseSelector("changePhase", 0, { phase: "battle" })),
        makeScriptedStep(makeResponseSelector("declareAttack", 0, { attackerUid: "p0-deck-100-0", targetUid: "p1-deck-200-0" })),
        makeScriptedStep(makeResponseSelector("passAttack", 1)),
        makeScriptedStep(makeResponseSelector("activateEffect", 0, { effectId: "fixture-remove-only-monster-target" }), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro keeps turn-player attack-response priority restorable before the only-monster target-removal quick effect",
            waitingFor: 0,
            windowId: 3,
            windowKind: "battle",
            pendingBattle: true,
            currentAttack: true,
            battleWindow: { kind: "attackNegationResponse", step: "attack", attackerUid: "p0-deck-100-0", targetUid: "p1-deck-200-0", responsePlayer: 0 },
            attackPasses: [1],
            legalActionCounts: { 0: 2, 1: 0 },
            legalActionGroupCounts: { 0: 2, 1: 0 },
            legalActions: [
              { type: "activateEffect", player: 0, windowId: 3, windowKind: "battle", effectId: "fixture-remove-only-monster-target", count: 1 },
              { type: "passAttack", player: 0, windowId: 3, windowKind: "battle", count: 1 },
            ],
            legalActionGroups: [
              effectGroup(0, "fixture-remove-only-monster-target", 1, 3),
              passBattleGroup(0, "passAttack", 1, 3),
            ],
          },
          after: {
            source: "edopro",
            note: "EDOPro keeps the attack-response window active after the only-monster attack target leaves",
            waitingFor: 1,
            windowId: 4,
            windowKind: "battle",
            pendingBattle: true,
            battleWindow: { kind: "attackNegationResponse", step: "attack", attackerUid: "p0-deck-100-0", targetUid: "p1-deck-200-0", responsePlayer: 1 },
            locations: { monsterZone: ["100"], graveyard: ["200"] },
            legalActionCounts: { 0: 0, 1: 1 },
            legalActionGroupCounts: { 0: 0, 1: 1 },
            legalActions: [{ type: "passAttack", player: 1, windowId: 4, windowKind: "battle", count: 1 }],
            legalActionGroups: [passBattleGroup(1, "passAttack", 1, 4)],
            logIncludes: ["Fixture only-monster target left before replay"],
          },
        }),
        makeScriptedStep(makeResponseSelector("passAttack", 1)),
        makeScriptedStep(makeResponseSelector("passAttack", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0)),
        makeScriptedStep(makeResponseSelector("passDamage", 1)),
        makeScriptedStep(makeResponseSelector("passDamage", 0), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro keeps the final end-damage-step pass restorable before only-monster replay choices are filtered",
            waitingFor: 0,
            windowId: 15,
            windowKind: "battle",
            pendingBattle: true,
            currentAttack: true,
            battleWindow: { kind: "endDamageStep", step: "damage", attackerUid: "p0-deck-100-0", targetUid: "p1-deck-200-0", responsePlayer: 0 },
            damagePasses: [1],
            locations: { monsterZone: ["100"], graveyard: ["200"] },
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [{ type: "passDamage", player: 0, windowId: 15, windowKind: "battle", count: 1 }],
            legalActionGroups: [passDamageGroup(0, 1, 15)],
          },
          after: {
            source: "edopro",
            note: "EDOPro offers cancel only because ONLY_ATTACK_MONSTER forbids direct replay",
            waitingFor: 0,
            windowId: 16,
            windowKind: "battle",
            pendingBattle: true,
            currentAttack: true,
            battleWindow: { kind: "replayDecision", step: "attack", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [{ type: "cancelAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 16, windowKind: "battle", count: 1 }],
            legalActionGroups: [
              {
                player: 0,
                label: "Attacks",
                windowId: 16,
                windowKind: "battle",
                count: 1,
                actions: [{ type: "cancelAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 16, windowKind: "battle", count: 1 }],
              },
            ],
            absentLegalActions: [{ type: "replayAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 16, windowKind: "battle" }],
            absentLegalActionGroups: [absentAttackGroup("p0-deck-100-0", undefined, undefined, 16)],
            logIncludes: ["Replay decision pending"],
          },
        }),
        makeScriptedStep(makeResponseSelector("cancelAttack", 0, { attackerUid: "p0-deck-100-0" }), {
          snapshotRestore: "both",
          before: {
            source: "edopro",
            note: "EDOPro keeps only-monster replay cancellation restorable without exposing direct replay",
            waitingFor: 0,
            windowId: 16,
            windowKind: "battle",
            pendingBattle: true,
            currentAttack: true,
            battleWindow: { kind: "replayDecision", step: "attack", attackerUid: "p0-deck-100-0", responsePlayer: 0 },
            locations: { monsterZone: ["100"], graveyard: ["200"] },
            legalActionCounts: { 0: 1, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [{ type: "cancelAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 16, windowKind: "battle", count: 1 }],
            legalActionGroups: [replayAttackGroup([{ attackerUid: "p0-deck-100-0", cancel: true }], 1, 16)],
            absentLegalActions: [{ type: "replayAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 16, windowKind: "battle" }],
            absentLegalActionGroups: [absentAttackGroup("p0-deck-100-0", undefined, undefined, 16)],
          },
          after: {
            source: "edopro",
            note: "EDOPro returns only-monster replay cancel to the Battle Phase open window",
            phase: "battle",
            waitingFor: 0,
            windowId: 17,
            windowKind: "open",
            pendingBattle: false,
            currentAttack: false,
            battleWindow: null,
            locations: { monsterZone: ["100"], graveyard: ["200"] },
            legalActionCounts: { 0: 2, 1: 0 },
            legalActionGroupCounts: { 0: 1, 1: 0 },
            legalActions: [
              { type: "changePhase", player: 0, windowId: 17, windowKind: "open", count: 1 },
              { type: "endTurn", player: 0, windowId: 17, windowKind: "open", count: 1 },
            ],
            legalActionGroups: [turnGroup(17)],
            absentLegalActions: [{ type: "declareAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 17, windowKind: "open" }],
            absentLegalActionGroups: [absentOpenAttackGroup(0, "p0-deck-100-0", 17)],
            logIncludes: ["Canceled replay attack"],
          },
        }),
      ],
      expected: {
        source: "edopro",
        note: "EDOPro final fixture state after canceling only-monster replay returns to open Battle Phase timing",
        phase: "battle",
        waitingFor: 0,
        windowId: 17,
        windowKind: "open",
        pendingBattle: false,
        currentAttack: false,
        battleWindow: null,
        locations: { monsterZone: ["100"], graveyard: ["200"] },
        legalActionCounts: { 0: 2, 1: 0 },
        legalActionGroupCounts: { 0: 1, 1: 0 },
        legalActions: [
          { type: "changePhase", player: 0, windowId: 17, windowKind: "open", count: 1 },
          { type: "endTurn", player: 0, windowId: 17, windowKind: "open", count: 1 },
        ],
        legalActionGroups: [turnGroup(17)],
        absentLegalActions: [{ type: "declareAttack", player: 0, attackerUid: "p0-deck-100-0", windowId: 17, windowKind: "open" }],
        absentLegalActionGroups: [absentOpenAttackGroup(0, "p0-deck-100-0", 17)],
        logIncludes: ["Fixture only-monster target left before replay", "Replay decision pending", "Canceled replay attack"],
      },
    };

    expect(runScriptedDuelFixture(fixture, { cardReader: createCardReader(cards) })).toEqual({ ok: true, failures: [] });
  });
});
